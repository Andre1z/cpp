#include <iostream>

int main() {
	bool comparacion = 8<7;
    std::cout << comparacion << std::endl;
    comparacion = 8>7;
    std::cout << comparacion << std::endl;
    comparacion = 8<=7;
    std::cout << comparacion << std::endl;
    comparacion = 8>=7;
    std::cout << comparacion << std::endl;
    comparacion = 8==7;
    std::cout << comparacion << std::endl;
    comparacion = 8!=7;
    std::cout << comparacion << std::endl;
    return 0;
}
